def quarter_of(month):
    first_quarter = 1, 2, 3
    second_quarter = 4,5,6
    third_quarter = 7,8,9
    forth_quarter = 10,11,12
    if month == first_quarter:
        return 1
    elif month == second_quarter:
        return 2
    elif month == third_quarter:
        return 3
    elif month == forth_quarter:
        return 4